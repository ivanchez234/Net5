#include <cmath>
#include "Header.h"

double sum(double a, double b)
{
	return a + b;
}
double min(double a, double b)
{
	return a - b;
}
double multip(double a, double b)
{
	return a * b;
}
double div(double a, double b)
{
	return a / b;
}
double exp(double a, double b)
{
	return std::pow(a, b);
}
